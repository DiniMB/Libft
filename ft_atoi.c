#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	number;
	int	sinais;

	i = 0;
	number = 0;
	sinais = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sinais = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number *= 10;
		number += str[i] - 48;
		i++;
	}
	return (number * sinais);
}
